library(Cairo)
Cairo(file="generated/graphs/pie-base-orgsizepro.png", type="png", units="px", width=800, height=480, dpi=72)
slices <- c(66, 61, 23, 17, 15, 13, 9, 2)
lbls <- c("2-10", "<= 1", "10-25", "26-100", "501-1500", "101-500", "1501-10000", "> 10000")
pct <- round(slices/sum(slices)*100)
lbls <- paste(lbls, " (", sep="")
lbls <- paste(lbls, pct)
lbls <- paste(lbls, "% )", sep="")
pie(slices, labels=lbls, col=rainbow(length(lbls)), main="How big is your organization?")
invisible(dev.off())
