library(Cairo)
Cairo(file="generated/graphs/pie-base-usetype.png", type="png", units="px", width=800, height=480, dpi=72)
slices <- c(254, 151, 131, 67)
lbls <- c("For hobby projects", "Main professional tool", "I don't yet use Haxe", "Occasional professional tool")
pct <- round(slices/sum(slices)*100)
lbls <- paste(lbls, " (", sep="")
lbls <- paste(lbls, pct)
lbls <- paste(lbls, "% )", sep="")
pie(slices, labels=lbls, col=rainbow(length(lbls)), main="What do you use Haxe for?")
