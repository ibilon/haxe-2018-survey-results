library(Cairo)
Cairo(file="generated/graphs/pie-base-partner.png", type="png", units="px", width=800, height=480, dpi=72)
slices <- c(565, 12)
lbls <- c("No", "Yes")
pct <- round(slices/sum(slices)*100)
lbls <- paste(lbls, " (", sep="")
lbls <- paste(lbls, pct)
lbls <- paste(lbls, "% )", sep="")
pie(slices, labels=lbls, col=rainbow(length(lbls)), main="Are you currently a Haxe Foundation support partner?")
invisible(dev.off())
