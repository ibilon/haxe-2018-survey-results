library(Cairo)
Cairo(file="generated/graphs/pie-extended-planinterest.png", type="png", units="px", width=800, height=480, dpi=72)
slices <- c(38, 6, 5, 4, 3, 2, 2)
lbls <- c("Professional", "Enterprise", "Elite", "Bounty", "If I Could", "Individual", "Paid")
pct <- round(slices/sum(slices)*100)
lbls <- paste(lbls, " (", sep="")
lbls <- paste(lbls, pct)
lbls <- paste(lbls, "% )", sep="")
pie(slices, labels=lbls, col=rainbow(length(lbls)), main="Which of the following support plans would you be interested in?")
invisible(dev.off())
