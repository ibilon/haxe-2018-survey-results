library(Cairo)
Cairo(file="generated/graphs/pie-base-macros.png", type="png", units="px", width=800, height=480, dpi=72)
slices <- c(246, 221, 107)
lbls <- c("Yes", "No", "What's a macro?")
pct <- round(slices/sum(slices)*100)
lbls <- paste(lbls, " (", sep="")
lbls <- paste(lbls, pct)
lbls <- paste(lbls, "% )", sep="")
pie(slices, labels=lbls, col=rainbow(length(lbls)), main="Do you use macros?")
invisible(dev.off())
