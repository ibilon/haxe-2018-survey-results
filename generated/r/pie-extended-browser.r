library(Cairo)
Cairo(file="generated/graphs/pie-extended-browser.png", type="png", units="px", width=800, height=480, dpi=72)
slices <- c(326, 209, 21, 14, 8, 7, 6, 1, 1)
lbls <- c("Chrome", "Firefox", "Safari", "Opera", "Edge", "Other", "Vivaldi", "Multiple", "Internet Explorer")
pct <- round(slices/sum(slices)*100)
lbls <- paste(lbls, " (", sep="")
lbls <- paste(lbls, pct)
lbls <- paste(lbls, "% )", sep="")
pie(slices, labels=lbls, col=rainbow(length(lbls)), main="What is your primary web browser?")
invisible(dev.off())
